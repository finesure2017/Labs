% Create matrix A
%  [1 2 3
%   4 5 6
%   7 8 9]
%----------------------------------------------------------------------------------------
'Basic 3 x 3 matrix'
A = [1 2 3; 4 5 6; 7 8 9]
'Access 2nd row of A'
% note: Matlab uses 1-indexing instead of 0-indexing
k = 2;
A(k, :)
'Access the 2nd column of A'
A(:, k)
%----------------------------------------------------------------------------------------
'An arbitrary size matrix'
A = [1 0 -2 3 4 0; 0 1 2 4 0 -2; 2 -1 -4 0 -2 8; -3 0 6 -8 -12 2]
'Manually produce reduced row echelon form'
'Fix 1st col'
% '3R - 2 * 1R' 
A(3,:) = A(3,:) - 2*A(1,:);
A(4,:) = A(4,:) + 3*A(1,:)
'Fix 2nd col'
A(3,:) = A(3,:) + A(2,:);
A(3,:) = A(3,:)/2
'Fix 3rd col'
A(1,:) = A(1,:) + 2*A(3,:);
A(2,:) = A(2,:) - 2*A(3,:)
'Fix 4th col'
A(1,:) = A(1,:) - A(4,:);
A(2,:) = A(2,:) - 6*A(4,:);
A(3,:) = A(3,:) + A(4,:)
if A == rref(A)
	'Succeed' 
end 
